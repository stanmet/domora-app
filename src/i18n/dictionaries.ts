// Название категории на языке интерфейса по slug из базы.
// Для неизвестных slug возвращается имя из базы (en или ru).
export function categoryLabel(t: Dict, slug: string, fallback: string): string {
  const map: Record<string, string> = {
    chef: t.catChef,
    clean: t.catClean,
    handy: t.catHandy,
    massage: t.catMassage,
    beauty: t.catBeauty,
    events: t.catEvents,
    other: t.catOther,
  };
  return map[slug] ?? fallback;
}

export function categoryDescription(t: Dict, slug: string): string {
  const map: Record<string, string> = {
    chef: t.catChefP,
    clean: t.catCleanP,
    handy: t.catHandyP,
    massage: t.catMassageP,
    beauty: t.catBeautyP,
    events: t.catEventsP,
    other: t.catOtherP,
  };
  return map[slug] ?? "";
}

// Подпись единицы оплаты для PriceUnit из Prisma.
export function unitLabel(t: Dict, unit: string): string {
  const map: Record<string, string> = {
    PER_GUEST: t.perGuest,
    PER_M2: t.perM2,
    PER_HOUR: t.perHour,
    PER_SESSION: t.perSession,
    PER_EVENT: t.perEvent,
    FIXED_QUOTE: t.byQuote,
  };
  return map[unit] ?? unit;
}
